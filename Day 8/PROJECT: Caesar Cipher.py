def caesar(t_text, shift_amount, direct):
  l_text = ""
  if direct == "decode":
    shift_amount *= -1
  for i in l_text:
    if i in alphabet:    
      position = alphabet.index(i)
      new_position = position + shift_amount
      l_text += alphabet[new_position]
    else:
      l_text = l_text+i
  print(f"\nThe {direct}d result: {l_text}")

